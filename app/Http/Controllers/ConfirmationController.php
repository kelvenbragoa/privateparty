    {
        //
        $data = $request->all();

        $guest = Guest::where('code',$data['code'])->first();

        if($guest != null){

            $guest->update([
                'is_confirmed'=>1
            ]);
            
            return view('confirmation',compact('guest'));
        }else{
           
            return view('confirmation',compact('guest'));
        }
    }

